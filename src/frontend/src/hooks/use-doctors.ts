function haversineDistanceKm(
  lat1: number,
  lon1: number,
  lat2: number,
  lon2: number,
): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLon = ((lon2 - lon1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) * Math.sin(dLat / 2) +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLon / 2) *
      Math.sin(dLon / 2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
  return R * c;
}

export function useDoctors() {
  const getNearbyDoctors = (
    userLat?: number,
    userLon?: number,
    topN = 5,
  ): DoctorInfo[] => {
    if (userLat === undefined || userLon === undefined) {
      return MOCK_DOCTORS.slice(0, topN).map((d) => ({
        ...d,
        distanceKm: undefined,
      }));
    }

    const withDistance = MOCK_DOCTORS.map((d) => ({
      ...d,
      distanceKm: Number.parseFloat(
        haversineDistanceKm(userLat, userLon, d.lat, d.lon).toFixed(1),
      ),
    }));

    return withDistance
      .sort((a, b) => (a.distanceKm ?? 0) - (b.distanceKm ?? 0))
      .slice(0, topN);
  };

  return { getNearbyDoctors, allDoctors: MOCK_DOCTORS };
}
